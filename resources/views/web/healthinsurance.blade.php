          <b>Factors for consideration before buying health insurance plans</b>
          
          <p class="para">With so many health insurance plans to choose from, it's easy to become confused about which one is right for you. Here are some things to think about while choosing the medical insurance that best suits your needs.</p>
          
          <ul>
              <li>
               <b>Claim settlement process:</b>
               <p>Before purchasing a health insurance policy, review the insurer's claim process; the simpler the claim process, the sooner the claim will be settled. Look for after-sales service as well. When obtaining health insurance, excellent customer service is critical. The person should choose an insurance company that offers a quick claim settlement.</p>
              </li>
              <li>
               <b>Policyholder's Age:</b>
               <p> When purchasing health insurance for oneself or family members, the buyer must grasp the significance of the age element. There are a variety of plans available according on the buyer's age, as well as premiums, waiting periods, and renewability. </p>
              </li>
              <li>
               <b>Medical History of Family Members:</b>
               <p> When purchasing a health insurance policy, it is important to evaluate and discuss the medical history of family members because it can affect the policy's price. If one of the family members already has a health problem, the chances of filing a claim for insurance increase.</p>
              </li>
              <li>
               <b>Deductibles:</b>
               <p>Before purchasing any form of health insurance policy, it is critical to research the policy's deductibles. A deductible is a sum of money that the insured must pay as part of a claim when arises, with the insurance provider covering the rest.</p>
              </li>
              <li>
               <b>Waiting Period:</b>
               <p>In the context of a health insurance policy, the waiting period refers to the amount of time you must wait before receiving the benefits of your coverage. The length of the waiting period varies by plan. </p>
              </li>
              <li>
                  <b>Hospitals in the Network:</b>
                  <p>When purchasing an insurance policy, the consumer must select the insurance provider that covers the most hospitals on their list. </p>
              </li>
              <li>
                  <b>Sum assured:</b>
                  <p>The sum assured is the amount of money that the insured person will receive at the conclusion of the insurance period. The sum insured is the amount paid to the insured in the event of an unforeseeable occurrence like as a medical emergency, theft, or car damage. When purchasing a health plan, consider the sum assured amount, which covers medical bills. Age, income, the number of family members, pre-existing ailments, and other factors all play a role in determining the appropriate medical insurance sum insured.</p>
              </li>
              <li>
                  <b>Renewability for the rest of one's life:</b>
                  <p> Different Health Insurance Plans have different possibilities for renewability. As a result, you must exercise caution when purchasing one for yourself or a family member.</p>
              </li>
              <li>
               <b>Cashless treatment:</b>
               <p> Choose an insurer that allows you to receive cashless treatment at one of its network hospitals. It's best to choose with a company that has a large network of cashless hospitals. Cashless treatment eliminates the bother of paying hospital bills because the insurer pays the medical fees directly to the hospital. Note that deductibles (if any) must be paid by the policyholder during claim settlement.</p>
              </li>
              <li>
               <b>Maternity cover:</b>
               <p> Medical costs associated with pregnancy can be rather significant. As a result, choose a medical plan that includes maternity coverage. Check the waiting period for maternity claims, as well as the policy's sub-limit.</p>
              </li>
              <li>
               <b>Medical check-up and value-added services:</b>
               <p> Look for a health plan that includes free medical exams. Some insurance also offer value-added services like lab testing and medication discounts.</p>
              </li>
              <li>
               <b>Exclusions:</b>
               <p> A insurance exclusion is a provision that prevents coverage for a specific risk. Pre-existing conditions, pregnancy, cosmetic treatment, medical expenses to treat injuries, alternative treatments, lifestyle-related disorders, hospital cost constraints, and diagnostic testing are all frequent exclusions in most health insurance policies. As a result, while purchasing health insurance, the customer must address these exclusions with the insurance provider.</p>
              </li>
              
          </ul>
