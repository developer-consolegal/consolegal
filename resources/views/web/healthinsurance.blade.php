<section class="types-of-healt-insurance" id="tab2">
    <div class="container-fluid container-md">
    <h3 class="title">Types of Health Insurance Plans in India</h3>
    <p class="para">Health insurance policies are of two types-</p>
    
    <ol>
        <li>
            <b>Indemnity Plans</b>
            <p>Indemnity plans are traditional health insurance policies that allow the policyholder to claim hospital expenditures up to the sum insured. Multiple claims can be made by policyholder until the maximum limit is reached. Your insurance company will pay for your medical bills in one of the two ways: </p>
            <ul>
                <li>
                    <b>Reimbursement Facility-</b>
                    You pays the costs first, and then the insurance company reimburses you.
                </li>
                <li><b>Cashless Facility-</b>You won't have to pay any bills because your insurance company will pay them directly to the hospitals.</li>
            </ul>
            <br />
            <p class="para">
                The following are the sorts of medical insurance policies that fall into the category of indemnity plans are:
                <ol style="list-style-type:lower-alpha;">
                    <li><b>Mediclaim Insurance</b>
                    <p>Illnesses and mishaps do not require advance notice. The same may be said regarding the costs incurred when a person is admitted to a hospital for any of the reasons. In the event of an illness or an accident, the Mediclaim Policy ensures that you are compensated for your hospitalisation expenditures. It covers in-patient costs such as doctor's fees, nursing, surgery and oxygen among other things. Mediclaim Policy is a type of insurance that is offered in the market as group medical insurance, individual medical insurance, overseas medical insurance, and so on</p>
                    </li>
                    <li><b>Individual Coverage</b>
                    <p>This insurance policy is for a single person, as the name implies. Other family members, such as spouses, children, parents, and so on, can be added for an additional fee. Each insured person will be assigned a certain sum assured amount. For example, if you have Rs.1 lakh individual health insurance policy and your wife is also insured, you can each claim up to Rs.1 lakh. This sort of insurance covers your medical expenses such as hospitalisation, surgery, room rent, and day-care operations, among other things.</p>
                    <p>Furthermore, premiums are calculated based on an individual's age, medical history, and sum insured. A claim lodged by one member of an individual plan has no bearing on the sum insured of the other insured member. If you are single or do not have children, you can purchase an individual health plan for yourself and your spouse and benefit from the sum covered on an individual basis. Because of the complete coverage and the sum assured that is provided, the premium is slightly greater than a family floater plan. If you are insured for an existing disease, however, there is a 2-year waiting time before you can receive payments.</p>
                    
                    <p><b>Features of an Individual Health insurance Plan</b></p>
                    <ul>
                        <li>This plan is available to people between the ages of 18 and 65.</li>
                        <li>Covers hospitalisation due to illness/injury, surgeries, room rent, day-care treatment, etc.</li>
                        <li>Basically it covers an individual but there is an option to buy separate policies to cover each family member.</li>
                        <li>Amount of the individual sum assured for each covered member.</li>
                    </ul>
                    </li>
                    
                    <li class="mt-3"><b>Family Floater Coverage</b>
                    <p>The Family Floater Health Plan is a low-cost insurance plan that covers your entire family, including your spouse, children, and elderly relatives, under one policy. Only one family member is responsible for paying the premium, and the complete family is covered for a single fee. This plan has a lower rate than individual health insurance policies that cover the entire family. The insured amount is distributed evenly among the members. If two family members require treatment at the same time, you can file a claim for both of them until the maximum is met. The premium is calculated based on the eldest person who will be covered under the plan. As a result, try to avoid including family members over the age of 60 in your family health insurance plan because they are more likely to become ill, and thus the premium will be affected.</p>
                    
                    
                     <p><b>Features of Family Floater Health insurance Plan</b></p>
                    <ul>
                        <li>Amount guaranteed by a floating sum.</li>
                        <li>It covers hospitalisation, treatment costs, surgeries, and childcare treatment, among other things.</li>
                        <li>A single policy is sufficient to cover the entire family.</li>
                    </ul>
                    </li>
                    
                    <li class="mt-3"><b>Senior Citizen Coverage</b>
                    
                    <p>This sort of health insurance in India covers persons aged 60 and above .If you want to acquire an insurance coverage for your parents or grandparents, this is the finest option. The Senior Citizen Health Insurance will cover the costs of pre- and post-hospitalization as well as medicines, whether due to a medical condition or an accident. Coverage under the plan depends upon the policy's terms and conditions. The premium for this plan is greater since elderly persons require more medical attention and are more likely to file a claim. The maximum age limit has been set at 70 years old. Before selling Senior Citizen Health Insurance, the insurer might also request a comprehensive physical examination. </p>
                    
                    <p><b>Features of Senior Citizen Health Insurance Plan</b></p>
                    <ul>
                        <li>Pre and post-hospitalization as a result of an accident or sickness, medical treatment, hospitalisation, and psychiatric treatment are covered. </li>
                        <li>A physical examination may not be required in order to acquire the coverage (depends on the terms & conditions of the insurer).</li>
                        <li>It provides coverage for residents over the age of 60.</li>
                    </ul>
                    </li>
                    <li class="mt-3"><b>Unit Linked Health Plans</b>
                    <p>ULIPs (Unit Linked Insurance Plans) are a type of unit-linked insurance plan. In these plans, a portion of your payment is invested, while the remainder is utilised to purchase health insurance. As a result, in addition to providing a safety net, this plan assists you in earning a profit. With the escalating expense of health care, your funds may be depleted. As a result, having more money at your disposal is always preferable. Because ULIPs are susceptible to market risks, they do not guarantee a fixed amount. ULIP returns are paid to the buyer at the end of the policy term. The returns are determined by the market's performance. </p>
                    
                    </li>
                    <li class="mt-3"><b>Group Health Insurance</b>
                    <p>
                        Employer-sponsored group health insurance policies are becoming common among medium and large businesses. This strategy is intended for a group of people that work for a company. The company's employer purchases group health insurance for its employees in order to cover their health insurance needs. This can be called an important advantages provided by organisation to its employees. Employers safeguard their employees for a financial catastrophe and prudence with the help of health insurance plan. This policy aids in the retention of talent inside a company. This policy's premium is cheaper than that of an Individual Health Insurance Policy.
                    </p>
                    <p><b>Features of a Group Health insurance Plan
</b></p>
                    <ul>
                        <li>The policy covers the employee and his or her dependents for medical expenses. </li>
                        <li>An employee's basic coverage is free.</li>
                        <li>Covers hospitalisation due to illness or injury, as well as urgent illnesses.</li>
                        <li>Maternity, OB/GYN, and day-care treatment are all covered by some group insurance policies.</li>
                        <li>There is no waiting period for benefits under the policy.</li>
                        <li>Floater sum is guaranteed.</li>
                        <li>Has the ability to increase the amount insured.</li>
                    </ul>
                    </li>
                </ol>
            </p>
        </li>
        <li>
            <b>Defined-Benefit Plans</b>
            <p>A defined-benefit plan, on the other hand, compensates the insured with a lump sum payment upon the identification of illness. Fixed Benefits provides you with a set amount of money in the event of a certain health issue caused by an accident or disease. It covers the health issues that are stated when the coverage is purchased. The following are some of the most popular health insurance policies that are covered by fixed benefits: </p>
            
            <p>The following are the sorts of medical insurance policies that fall into the category of Defined-benefit plans are:</p>
            
            <ul style="list-style-type:disc;">
                
                <li>
                    <b>Critical Illness Plan</b>
                    <p>The Critical Illness Insurance plan protects a person by providing a lump sum payment in the event of a life-threatening illness. This strategy is tailored to address a specific ailment. With the prevalence of lifestyle diseases on the rise, it's more important than ever to protect yourself from these ailments. Because treating chronic conditions can be costly, especially for a middle-class household, purchasing crucial insurance might help to minimise those costs to some extent. It pays a pre-determined sum toward treatment when an illness is detected, regardless of pre- or post-hospitalization expenses. The chosen health problems are included when you acquire the insurance, and if you are diagnosed with any of the pre-selected ailments, you can collect your insurance benefits. Since the expenses of treating these ailments are so costly, the Critical Illness Policy provides a monetary wellbeing net in the event that you are determined to have one of the sicknesses as listed below:
                    <ul>
                    <li>Major organ transplant</li>
                    <li>Cancer</li>
                    <li>Stroke</li>
                    <li>Aorta graft surgery</li>
                    <li>Multiple sclerosis</li>
                    <li>Kidney failure</li>
                    <li>Paralysis</li>
                    <li>Coronary artery bypass surgery</li>
                    <li>First heart attack</li>
                    <li>Primary pulmonary arterial hypertension</li>
                    </ul>
                    
                    <br />
                    <b>Features of Critical Illness insurance Plan</b>
                    
                    <ul>
                        <li>Provides a one-time lump sum benefit (up to the total insured) when a serious illness is diagnosed.</li>
<li>It covers critical illnesses like heart attacks, strokes, kidney failure, paralysis, and organ transplantation, among others.</li>
<li>To be eligible for insurance benefits, the insured must live for at least 30 days</li>
<li>Some plans also provide free health screenings.</li>
                    </ul>
                    
                    </p>
                </li>
                <li>
                    <b>Hospital Daily Cash</b>
                    <p>Some health insurance policies include this coverage as an in-built benefit as part of their overall coverage. This plan might help you protect yourself from unexpected costs while you're in the hospital. Apart from the hospitalisation charges, the insured is granted a daily cash allowance up to a set level under this policy. The usual hospital charges are not fixed after a person is admitted to the hospital, and they tend to fluctuate depending on the patient's condition. The Hospital Daily Cash is the finest option for an individual in this situation. This plan provides a daily cash benefit ranging from Rs. 500 to Rs. 10,000, depending on the coverage level chosen at the time of insurance. If a person is admitted to the hospital for more than seven days, several plans provide benefits during recovery period.</p>
                </li>
                <li>
                    <b>Personal Accident Plan </b>
                    <p>Accidents can happen at any time, and the number of road accident cases has risen over time, which is why there are now certain types of health insurance in India to safeguard citizens. A Personal Accident Insurance Policy covers medical expenditures incurred because of a mishap. As a result, purchasing a personal accident insurance coverage is a good idea because such catastrophes can deplete your finances owing to the medical costs associated with treating the injuries. In the event of death or loss of income owing to permanent partial or whole disability, a lump sum payment is made to the insured or their family. The policy also covers the insured's partial disability, permanent disability, and death as a result of an accident. Some of the plans also provide education and orphan benefits, which help to pay the costs of raising children. Apart from that, if the insured is involved in an accident and has any loan obligations, the insurance company will take care of them.
</p>
                <b>Features of Personal Accident Insurance Plan</b>
<ul>
    <li>The policy covers you in the event of an accident, a temporary or permanent disability, or death.</li>
    <li>Death benefit to the nominee (up to the sum covered) in the event of the insured's death.</li>
    <li>It pays for hospitalisation in the event of an accident.</li>
    <li>It pays for the school expenditures of the insured's dependent kid in the event of the insured's death.</li>
    <li>It pays out if the insured loses his or her job due to a permanent disability.</li>
    <li>It pays for ambulance services.</li>
    <li>In the event of hospitalisation due to an accident, the coverage pays a daily- cash allowance.</li>
</ul>
                </li>
            </ul>
        </li>
    </ol>
    
    
