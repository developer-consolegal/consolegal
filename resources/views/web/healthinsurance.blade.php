        <h3 class="title ">Reasons to Buy Health Insurance:</h3>
        <p class="para">Apart from the ever-increasing medical costs, health insurance is purchased for the following reasons:</p>
        
        <br />
        
        
        <ol>
            <li>
                <b>Comes with Lower Premium</b>
                <p>
The cost of health insurance is significantly lower now than it will be later in life. Because you're less likely to get sick at this time, insurance companies will charge you a lesser price for a health insurance plan because you're a low-risk eater. When you compare premiums in your 30s and 50s, you'll notice that the latter costs twice as much.
</p>
            </li>
            <li>
                <b>Easy to Get Insurance</b>
                <p>
It's difficult to get health insurance if you have a pre-existing condition. Even if you receive it, you'll have to pay a higher price. If you fail to disclose a condition at the time of purchase and then file a claim for a complication resulting from that disease, your claim will be rejected by default. Because you are less susceptible to such illnesses when you are young, getting health insurance is simple.
</p>
            </li>
            <li>
                <b>Increase Your Tax Savings</b>
                <p>The insured receives tax benefits for paying the health insurance premium under Section 80D of the Income Tax Act of 1961. When you get health insurance when you're young, you get a tax break for a longer length of time than if you buy it later in life.
</p>
            </li>
            <li>
                <b>Reap the Benefits of Savings</b>
                <p>In other words, health insurance is a type of investment that allows you to protect yourself financially from unforeseen events. If you start investing when you're young, you'll acquire the habit of saving later on. Because inflation has a significant impact on every aspect of our lives, developing a savings habit can help you cope with it to some level. 
</p>
            </li>
            <li>
                <b>No Waiting Period</b>
                <p>Health insurance has waiting periods, such as a 30-day initial waiting period, a 2- to 4-year waiting period for pre-existing illnesses, and so on. It may, however, differ from one insurer to the next. You do not need to file an insurance claim if you are in good health.  This manner, you can easily complete the waiting period and file your insurance without difficulty.
</p>
            </li>
            <li>
                <b>Avail Bonus</b>
                <p>
Health insurance comes with a bonus. One such prize is the No Claim Bonus, which ranges from 20% to 50% and is offered for not claiming your insurance. The NCB is granted as a discount on consecutive years' premiums or as an increase in the sum insured. When you're young, you have fewer odds of filing a claim, so you may maximise your NCB bonus and get the most out of your health insurance policy.
</p>
            </li>
        </ol>
